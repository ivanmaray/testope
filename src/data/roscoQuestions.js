const ROSCO_LETTERS = Object.keys(roscoQuestionBank);

const pickRandom = (items) => items[Math.floor(Math.random() * items.length)];

export const buildRoscoQuestionSet = () =>
  ROSCO_LETTERS.map((letra) => {
    const opciones = roscoQuestionBank[letra];
    const seleccion = pickRandom(opciones);
    return {
      letra,
      pista: seleccion.pista,
      respuesta: seleccion.respuesta,
      categoria: seleccion.categoria ?? 'Rosco',
    };
  });

export const ROSCO_TOTAL = ROSCO_LETTERS.length;

export default roscoQuestionBank;
